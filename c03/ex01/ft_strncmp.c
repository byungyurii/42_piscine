int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (*s1 && *s2 && n > 0)
	{
		if (*s1 == *s2)
		{
			s1++;
			s2++;
			n--;
			continue;
		}
		return (*s1 - *s2);
	}
	if (*s1 && n > 0)
	{
		return (*s1 - '0');
	}
	if (*s2 && n > 0)
	{
		return (-1 * (*s2 - '0'));
	}
	return (0);
}
